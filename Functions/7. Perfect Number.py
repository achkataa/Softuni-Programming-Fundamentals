number = int(input())


def perfect_or_not(n):
    result = 0
    for num in range(1, n):
        if n % num == int():
            result += num
    if result == number:
        return True
    else:
        return False

if perfect_or_not(number):
    print("We have a perfect number!")
else:
    print("It's not so perfect.")
